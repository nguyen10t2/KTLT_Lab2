#include <iostream>
#include <cmath>
#include <iomanip>

float get_hypotenuse(float x, float y) {
    return sqrt(x * x + y * y);
}

int main() {
    float x_dtn, y_dtn;
    std::cin >> x_dtn >> y_dtn;
    float z_dtn = get_hypotenuse(x_dtn, y_dtn);
    std::cout << std::fixed << std::setprecision(2) << z_dtn << std::endl;
    return 0;
}